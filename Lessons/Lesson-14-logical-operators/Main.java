
public class Main {

    public static void main(String[] args) {
        // && = and
        // || = or
        // ! = NOT

        int weight = 4;
        int otherNum = 1;

        if (weight == 4 && otherNum == 1) {
            System.out.println("Congratulations! Basic example");
        }

        if (weight == 2 || otherNum == 1) {
            System.out.println("Learning syntax is boring");
        }

        if (weight != 3) {
            System.out.println("Yup, still bored.");
        }
    }
}
